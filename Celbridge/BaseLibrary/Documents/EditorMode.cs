namespace Celbridge.Documents;

/// <summary>
/// Supported editor display modes.
/// </summary>
public enum EditorMode
{
    /// <summary>
    /// Display editor panel only.
    /// </summary>
    Editor,

    /// <summary>
    /// Display preview panel only.
    /// </summary>
    Preview,

    /// <summary>
    /// Display both editor and preview panels in a split view.
    /// </summary>
    EditorAndPreview
}
